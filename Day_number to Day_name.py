Day_no = int(input('Enter the number: '))

if Day_no == 0:
    print('Monday')
elif Day_no == 1:
    print('Tuesday')
elif Day_no == 2:
    print('Wednesday')
elif Day_no == 3:
    print('Thursday')
elif Day_no == 4:
    print('Friday')
elif Day_no == 5:
    print('Saturday')
else: 
    print('Sunday')